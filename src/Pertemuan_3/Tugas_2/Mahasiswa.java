package Pertemuan_3.Tugas_2;

public class Mahasiswa {
    public String nama;
    public long nim;
    public char kelamin;
    public double ipk;

    public Mahasiswa(String nama, long nim, char kelamin, double ipk){
        this.nama = nama;
        this.nim = nim;
        this.kelamin = kelamin;
        this.ipk = ipk;
    }
}
